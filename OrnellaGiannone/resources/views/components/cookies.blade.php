    <h1>INFORMATIVA SUI COOKIES</h1> <br><br>
    <p>Per far funzionare bene questo sito utilizziamo dei file dati chiamati “cookies”.</p> <br>
    <h3>Cosa sono i cookies</h3> <br>
    <p>Un cookie è un piccolo file di testo che viene salvato sul tuo computer o dispositivo mobile mentre visiti il sito. <br>
        Grazie ai cookies vengono memorizzate le tue azioni e preferenze (per es. pagine visualizzate, dimensioni dei caratteri e altre
        impostazioni di visualizzazione) in modo da velocizzare la navigazione quando torni sul sito o navighi da una pagina all’altra.
    </p>
    <br><br>
    <h3>Quali sono le principali tipologie di Cookies?</h3><br>
    <p><strong>Esistono due tipi di cookie:</strong></p> <br>
    <ul>
        <li>cookie proprietari: soggetti e leggibili dal solo dominio che li ha creati</li>
        <li>cookie di terze parti: soggetti e creati da domini esterni a quello che sta visitando l’utente</li>
    </ul>
    <br>
    <p>Questi a loro volta possono essere:</p><br>
    <p>
        <strong>a. Cookie tecnici</strong>
        <br>
        I cookie tecnici sono quelli utilizzati al solo fine di “effettuare la trasmissione di una comunicazione su una rete di comunicazione elettronica, 
        o nella misura strettamente necessaria al fornitore di un servizio della società dell’informazione esplicitamente richiesto dall’abbonato o dall’utente a erogare tale servizio”. <br>
        Essi non vengono utilizzati per scopi ulteriori e sono normalmente installati direttamente dal titolare o gestore del sito web. <br>
        Possono essere suddivisi in cookie di navigazione o di sessione, che garantiscono la normale navigazione e fruizione del sito web (permettendo, 
        ad esempio, di realizzare un acquisto o autenticarsi per accedere ad aree riservate).
    </p>
    <br>
    <p>
        <strong>b. Cookie di profilazione</strong>
        <br>
        I cookie di profilazione sono volti a creare profili relativi all’utente e vengono utilizzati al fine di inviare messaggi pubblicitari in linea con le 
        preferenze manifestate dallo stesso nell’ambito della navigazione in rete. In ragione della particolare invasività che tali dispositivi possono avere 
        nell’ambito della sfera privata degli utenti, la normativa europea e italiana prevede che l’utente debba essere adeguatamente informato sull’uso degli 
        stessi ed esprimere così il proprio valido consenso.
    </p>
    <br>
    <p>
        <strong>c. Cookie di analisi e prestazioni</strong>
        <br>
        Sono cookie utilizzati per raccogliere e analizzare il traffico e l’utilizzo del sito in modo anonimo. 
        Questi cookie, pur senza identificare l’utente, consentono,
        per esempio, di rilevare se il medesimo utente torna a collegarsi in momenti diversi. Permettono inoltre di monitorare il sistema e migliorarne le prestazioni e l’usabilità. 
        La disattivazione di tali cookie può essere eseguita senza alcuna perdita di funzionalità.
    </p>
