    <h3>Gestione dei cookie</h3><br>
    <p>
        L’utente può decidere se accettare o meno i cookie utilizzando le impostazioni del proprio browser. <br>
        <strong>Attenzione </strong> : la disabilitazione totale o parziale dei cookie tecnici può compromettere l’utilizzo delle funzionalità del sito 
        riservate agli utenti registrati. Al contrario, la fruibilità dei contenuti pubblici è possibile anche disabilitando completamente i cookie. <br>
        La disabilitazione dei cookie “terze parti” non pregiudica in alcun modo la navigabilità. <br><br>
        L’impostazione può essere definita in modo specifico per i diversi siti e applicazioni web. Inoltre i migliori browser consentono di definire impostazioni
        diverse per i cookie “proprietari” e per quelli di “terze parti”. <br><br>
        A titolo di esempio, in Firefox, attraverso il menu Strumenti → Opzioni → Privacy, è possibile accedere ad un pannello di controllo dove è possibile definire 
        se accettare o meno i diversi tipi di cookie e procedere alla loro rimozione.
