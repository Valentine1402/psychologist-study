<div class="speak">
    <h1>Adulti</h1>
    <p>La consulenza psicologica con adulti permette di approfondire i principali fattori che determinano una situazione di crisi personale. <br>
        <b>Attraverso alcuni colloqui clinici è possibile ipotizzare le principali motivazione del disagio ed identificare la forma di aiuto più adatta.</b><br>
        Si può richiedere una consultazione senza pensare che si(di essere?) è gravemente malati, ma per dar voce a un malessere che non sa esprimersi in altro modo se 
        non con un sintomo. <br>
        La terapia è finalizzata a migliorare la propria qualità della vita e ad essere maggiormente consapevoli del proprio Sè. <br>
        Generalmente la richiesta d’aiuto nasce quando non si sopporta più un lungo periodo di malessere cronico nel quale si sente di 
        <b>“aver toccato il fondo”</b> ma ci si sente impotenti, oppure in seguito alla comparsa improvvisa ed apparentemente immotivata, di sintomi in forma acuta. <br>
        Le situazioni che possono condurre ad una crisi sono:</p>
        <br>
        <ul>
            <li><i class="fas fa-check"></i> momenti di cambiamento personale o dei propri familiari;</li>
            <li><i class="fas fa-check"></i> la comparsa o il prolungarsi nel tempo di una malattia importante propria o dei propri cari; </li>
            <li><i class="fas fa-check"></i> episodi traumatici come ad esempio incidenti o perdite improvvise; </li>
            <li><i class="fas fa-check"></i> lutti o separazioni dal coniuge o dai figli.</li>
        </ul><br>
       <p>Tra i sintomi riconducibili a una sofferenza psichica e intaccano la qualità della vita si possono distinguere:</p>  <br>
        <ul>
            <li><i class="fas fa-check"></i> I disturbi dell’umore (umore depresso, stati di ansia, alternanza di periodi con umore basso ad altri con eccessiva euforia); </li>
            <li><i class="fas fa-check"></i> Gli attacchi di panico;</li>
            <li><i class="fas fa-check"></i> I disturbi o i disagi corporei non riconducibili ad una patologia medica come cefalee, disturbi dell’apparato digerente; </li>
            <li><i class="fas fa-check"></i> I disturbi del sonno;</li>
            <li><i class="fas fa-check"></i> Fobie e pensieri intrusivi o ossessivi;</li>
            <li><i class="fas fa-check"></i> Manifestazione di tic o comportamenti compulsavi e ripetitivi.</li>
        </ul>
