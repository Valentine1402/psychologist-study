<div class="speak">
    <h1 id="speakTitle">Adolescenti e Pre-Adolescenti</h1>
    <p>Una consultazione con l’adolescente è necessaria quando i sintomi manifesti invadono la normale crescita e lo sviluppo dell’identità sana. <br>
        Non si tratta dei segni di una funzionale e comune crisi passeggera, determinata dai cambiamenti psico/fisici, ma di segnali che evidenziano un 
        malessere o un disagio tale da rallentare o allontanare il ragazzo dalla propria vita. <br>
        La collaborazione dei genitori in questa fase è importantissima. <br>
        Il loro coinvolgimento attivo nel processo di cura è il primo supporto alle 
        difficoltà dei figli e una risorsa fondamentale per lo specialista.
        I sintomi che evidenziano la fragilità e la sofferenza di un adolescente per cui è necessaria una presa in carico tempestiva sono: 
         
    </p>
    <br>
      <ul>
            <li><i class="fas fa-check"></i> attacchi di panico e/o fobie;</li>
            <li><i class="fas fa-check"></i> stati d’ansia sociale o scolastica, con o senza ritiro;</li>
            <li> <i class="fas fa-check"></i> presenza di umore depresso oppure eccessiva euforia non giustificabile da eventi esterni,</li>
            <li><i class="fas fa-check"></i> ripetuti ed apparentemente immotivati scoppi di collera o di pianto, </li>
            <li><i class="fas fa-check"></i> diminuzione delle relazioni e/o ritiro sociale, </li>
            <li><i class="fas fa-check"></i> drastico calo della performance scolastica ;</li>
            <li><i class="fas fa-check"></i> consumo abituale o abuso di alcol e di sostante nocive; </li>
            <li><i class="fas fa-check"></i> calo o un aumento ponderale in assenza di un regime alimentare controllato;</li>
            <li><i class="fas fa-check"></i> disturbi psicosomatici (reazioni cutanee, dolori muscolari, cefalee, disturbi dell’apparato gastrointestinale quando non giustificate da 
                condizioni mediche favorevoli);</li>
            <li><i class="fas fa-check"></i> disturbi del sonno (risvegli notturni, difficoltà nell’addormentamento);</li>
            <li><i class="fas fa-check"></i> atti o pensieri ricorrenti volti a colpire l’integrità corporea;</li>
            <li><i class="fas fa-check"></i> agiti auto-lesivi, pensieri e tentativi di suicidio</li>
            <li><i class="fas fa-check"></i> pensieri ripetitivi e stereotipati con o senza attività compulsiva.</li>
        </ul> 
