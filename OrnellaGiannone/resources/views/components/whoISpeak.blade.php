<div class="speak">
    <h1>Genitori</h1>
    <p>Un supporto ai genitori può essere di grande aiuto all’intera famiglia. Infatti, solo così il ragazzo sentirà di non essere la causa delle 
        problematiche dell’intero nucleo e che non solo lui ma anche i genitori si impegnano affinché possa essere superata la situazione di malessere. <br>
        D’altro canto anche i genitori possono trarre beneficio da un supporto per loro, la presa in carico può essere pensata in setting diversi: <br>
        come un percorso individuale, di coppia o di gruppo. <br>
        Qualsiasi modalità permette di ricevere uno spazio di pensiero in cui si possono analizzare i vissuti emotivi che bloccano il processo di risoluzione. <br>
        L’aiuto di un occhio esterno è focalizzato al riconoscimento del conflitto e al recupero (o scoperta) delle  proprie risorse, lontani dal fantasma 
        del giudizio. <br>
        Il setting di gruppo può essere utile per condividere con altri genitori (che non si conoscono prima degli incontri) le difficoltà, 
        rispecchiarsi nelle fatiche, confrontarsi e sostenersi vicendevolmente e permette di non essere soli nel sentire il peso della responsabilità 
        che si avverte quando si diventa genitore e nel senso di colpa per gli errori che ci si auto-attribuisce. <br>
        Il percorso con i genitori può essere indicato sia quando l’adolescente accetta di seguire un percorso personale, sia quando rifiuta l’aiuto. 
    </p>
